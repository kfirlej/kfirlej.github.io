'use strict';

var liczba = 10;

// Operator przypisania

liczba += 5; // => liczba = liczba + 5;

// Operator porównania

console.log (liczba == 15); //rowne
console.log (liczba != 15); //różne

var liczbaStr = 15;

console.log(liczba === liczbaStr); //sprawdza zgodnosc wartosci i typ danych
console.log(liczba !== liczbaStr); //sprawdza zgodnosc wartosci i typ danych

// Operator warunkowy

var warunek = liczba % 2 == 0? 'parzysta' : 'nieparzysta';

console.log(warunek);










